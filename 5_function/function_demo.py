# Declaration

# Function without arguments
def summation():
    number1 = 10
    number2 = 5
    result = number1 + number2
    print(result)

# Calling
summation()

# Function with arguments
def subtract():
    number1 = int(input("Number 1: "))
    number2 = int(input("Number 2: "))
    result = number1 - number2
    print(result)

subtract()


